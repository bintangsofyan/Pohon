public class Petani {
    private int petani_id;
    private String petani_name;
    private String petani_address;
    private String petani_email;
    private String petani_uname;
    private String petani_password;
    private String persil_id;

    public int getPetaniId() {
        return petani_id;
    }

    public void setPetaniId(int petani_id) {
        this.petani_id = petani_id;
    }

    public String getPetaniName() {
        return petani_name;
    }

    public void setPetaniName(String petani_name) {
        this.petani_name = petani_name;
    }


    public String getPetaniAddres() {
        return petani_address;
    }

    public void setPetaniAddress(String petani_address) {
        this.petani_address = petani_address;
    }

    public String getPetaniEmail() {
        return petani_email;
    }

    public void setPetaniEmail(String petani_email) {
        this.petani_email = petani_email;
    }

    public String getPetaniUname() {
        return petani_uname;
    }

    public void setPetaniUname(String petani_uname) {
        this.petani_uname = petani_uname;
    }


    public String getPetaniPassword() {
        return petani_password;
    }

    public void setPetaniPassword(String petani_password) {
        this.petani_password = petani_password;
    }


    public String getPersilId() {
        return persil_id;
    }

    public void setPersilId(String persil_id) {
        this.persil_id = persil_id;
    }
}